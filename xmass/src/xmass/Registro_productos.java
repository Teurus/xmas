    private void bAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarActionPerformed
        
        PreparedStatement ps = null;
        try {
            Base_Datos objCon = new Base_Datos();
            Connection conn = objCon.getConectarDB();
            ps = conn.prepareStatement("INSERT INTO productos (SKU, Nombre, Cantidad, categoria, Precio) VALUES (?,?,?,?,?)");
            ps.setString(1, jSKU.getText());
            ps.setString(2, jNombre.getText());
            ps.setString(3, jCantidad.getText());
            ps.setObject(4, jCategoria.getSelectedItem());
            ps.setString(5, jPrecio.getText());

            ps.execute();

            JOptionPane.showMessageDialog(null, "Producto Guardado");
            Object[] fila = new Object[5];
            fila[0] = jSKU.getText();
            fila[1] = jNombre.getText();
            fila[2] = jCantidad.getText();
            fila[3] = jCategoria.getSelectedItem();
            fila[4] = jPrecio.getText();
            dtm.addRow(fila);

            limpiar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al Guardar Producto");
            System.out.println(ex);
        }
    

        
    }//GEN-LAST:event_bAgregarActionPerformed

    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        
        PreparedStatement ps = null;
        try {

            Base_Datos objCon = new Base_Datos();
            Connection conn = objCon.getConectarDB();

            int Fila = juwu.getSelectedRow();
            String SKU = juwu.getValueAt(Fila, 0).toString();

            ps = conn.prepareStatement("DELETE FROM productos WHERE SKU=?");
            ps.setString(1, SKU);
            ps.execute();

            dtm.removeRow(Fila);
            JOptionPane.showMessageDialog(null, "Producto Eliminado");
            limpiar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al Eliminar Producto");
            System.out.println(ex.toString());
        }
        
    }//GEN-LAST:event_bEliminarActionPerformed

    private void bEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditarActionPerformed
        
       int Fila = juwu.getSelectedRow();

        PreparedStatement ps = null;
        try {
            Base_Datos objCon = new Base_Datos();
            Connection conn = objCon.getConectarDB();
            ps = conn.prepareStatement("UPDATE productos SET Nombre=?, Cantidad=?, Categoria=?, Precio=? WHERE SKU=?");

            ps.setString(1, jNombre.getText());
            ps.setString(2, jCantidad.getText());
            ps.setObject(3, jCategoria.getSelectedItem());
            ps.setString(4, jPrecio.getText());
            ps.setString(5, jSKU.getText());
            
            ps.execute();

            JOptionPane.showMessageDialog(null, "Producto Modificado");
            juwu.setValueAt(jSKU.getText(), Fila, 0);
            juwu.setValueAt(jNombre.getText(), Fila, 1);
            juwu.setValueAt(jCantidad.getText(), Fila, 2);
            juwu.setValueAt(jCategoria.getSelectedItem(), Fila, 3);
            juwu.setValueAt(jPrecio.getText(), Fila, 4);

            limpiar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al Modificar Producto");
            System.out.println(ex);
        }
        
    }//GEN-LAST:event_bEditarActionPerformed

