    private void juwuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_juwuMouseClicked
        PreparedStatement ps = null;
        ResultSet rs = null;
        try {
            Base_Datos objCon = new Base_Datos();
            Connection conn = objCon.getConectarDB();

            int Fila = juwu.getSelectedRow();
            String codigo = juwu.getValueAt(Fila, 0).toString();

            ps = conn.prepareStatement("SELECT SKU, Nombre, Cantidad, Categoria, Precio FROM productos WHERE SKU=?");
            ps.setString(1, codigo);
            rs = ps.executeQuery();

            while (rs.next()) {
                jSKU.setText(rs.getString("SKU"));
                jNombre.setText(rs.getString("Nombre"));
                jCantidad.setText(rs.getString("Cantidad"));
                jCategoria.setSelectedItem(rs.getString("Categoria"));
                jPrecio.setText(rs.getString("Precio"));
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_juwuMouseClicked


