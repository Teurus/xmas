    private void llenarTabla(){
        DefaultTableModel qs = new DefaultTableModel (new String[]{"SKU","Nombre","Cantidad","Categoria","Precio"}, productos.size());
        tablav.setModel(qs);
        TableModel tm = tablav.getModel();
        for(int i = 0; i<productos.size();i++){
            Producto p = productos.get(i);
            tm.setValueAt(p.getSku(),i,0);
            tm.setValueAt(p.getNombre(),i,1);
            tm.setValueAt(p.getCantidad(),i,2);
            tm.setValueAt(p.getCategoria(),i,3);
            tm.setValueAt(p.getPrecio(),i,4);
            
        }
        
    }
    
     private void limpiar(){
        DefaultTableModel model = (DefaultTableModel) tablav.getModel();
        model.setRowCount(0);
     }
    
