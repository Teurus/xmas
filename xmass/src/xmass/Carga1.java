    public void cargarArchivo(File archivo){
        
        FileReader fr = null;
        BufferedReader br = null;
        
        try{
            fr = new FileReader(archivo);
            br = new BufferedReader(fr);
            
            String linea;
            
            while((linea=br.readLine())!=null){
                
                String arreglo [] = linea.split(";");
                if(arreglo.length==5){
                    Producto p = new Producto();
                    p.setSku(Integer.parseInt(arreglo[0]));
                    p.setNombre(arreglo[1]);
                    p.setCantidad(Integer.parseInt(arreglo[2]));
                    p.setCategoria(arreglo[3]);
                    p.setPrecio(Integer.parseInt(arreglo[4]));
                    productos.add(p);
                }
              
            
        }
            llenarTabla();
            
            
        }catch(Exception ex){
            ex.printStackTrace();
        }
        finally{
        try{
            if(fr!=null){
                fr.close();
            }
        }
        catch(Exception ex){
            ex.printStackTrace();
        }
    }
    }

